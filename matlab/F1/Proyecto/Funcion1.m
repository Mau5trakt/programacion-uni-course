% --- Executes on button press in pushbutton1. Boton de calcular 
function pushbutton1_Callback(hObject, eventdata, handles)
T = str2num(get(handles.edit7,'String')); %Definimos La temperatura
Diametro = str2num(get(handles.edit1,'String')); %Definimos el diametro
Velocidad = str2num(get(handles.edit8,'String')); %definimos la velocidad 

%Calcular la densidad y la viscosidad a aprtir de T 
densidad = 1.6e-05*T.^3 + -0.005963*T.^2 + 0.02056*T + 1000;
viscosidad = (-2.717e-09*T.^3 + 6.052e-07*T.^2 + -4.822e-05*T + 0.00176)*1000;

%Calcular Reynolds 
rey = (Velocidad*Diametro*densidad)/(1000*viscosidad);
set(handles.edit9,'String',rey)

%e = rugosidad/Diametro;

%if rey > 2000:
    
 %Determinar el tipo de regimen 
Re = (Velocidad*Diametro*densidad)/(1000*viscosidad);

if 0 < Re && Re < 2000

   a = 'Regimen laminar';

elseif  2000 <= Re && Re <= 4000
 
    a = 'Regimen Transitorio ';
   
elseif 4000 < Re
        
        a = 'Regimen turbulento';
            
else 
      a = 'Error: Reynolds no puede ser negativo';
   
  
end   

  set(handles.edit12,'string',(a)) 
  

